function setLang(lang) {
  const t = LANG[lang];
  if (!t) return;
  const isAr = lang === "ar";

  // Direction
  document.documentElement.lang = isAr ? "ar" : "en";
  document.documentElement.dir = isAr ? "rtl" : "ltr";

  // All data-i18n elements
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.dataset.i18n;
    if (t[key]) el.textContent = t[key];
  });

  // Toggle button
  const btn = document.getElementById("langToggle");
  if (btn) btn.textContent = t.lang_btn;

  // Update morph words
  if (window._morphWords) window._morphWords = t.morph;

  // Save
  localStorage.setItem("dejobe_lang", lang);
}

// Init
document.addEventListener("DOMContentLoaded", () => {
  const saved = localStorage.getItem("dejobe_lang") || "en";
  setLang(saved);

  const btn = document.getElementById("langToggle");
  if (btn) {
    btn.addEventListener("click", () => {
      const cur = document.documentElement.lang === "ar" ? "en" : "ar";
      setLang(cur);
    });
  }
});
